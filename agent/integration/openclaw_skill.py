    async def _handle_sync(self, args: str, msg: IncomingMessage) -> str:
        """Sync status."""
        if self.gateway:
            return self.gateway.get_status()
        return "⚠️ Gateway not connected."

    # ── Natural Language Query Handler ───────────────────────────────

    async def _handle_natural_query(self, text: str, msg: IncomingMessage) -> Optional[str]:
        """Handle finance-related natural language (not a slash command).

        Routes to search or data lookup based on content.
        """
        # Check for ticker mentions ($AAPL, $BTC)
        tickers = re.findall(r'\$([A-Z]{1,5})\b', text)
        if tickers and self._data_hub:
            results = []
            for ticker in tickers[:3]:
                try:
                    data = await self._data_hub.get_stock_price(ticker)
                    if data:
                        price = data.get("price", "?")
                        change = data.get("change", 0)
                        sign = "+" if change >= 0 else ""
                        results.append(f"**{ticker}** ${price} ({sign}{change})")
                except Exception:
                    pass
            if results:
                return "📊 " + " | ".join(results)

        # Fall back to search
        if self._search:
            try:
                result = await self._search.search(text, max_results=3)
                if result.items:
                    lines = ["📰 _Related news:_"]
                    for item in result.items[:3]:
                        lines.append(f"• [{item.title}]({item.url})")
                    return "\n".join(lines)
            except Exception:
                pass

        return None  # Couldn't handle it


